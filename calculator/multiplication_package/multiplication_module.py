def multiply_by_2(x):
    """Multiply a number by 2.
    
    Args:
        x: number to be doubled.
    
    Returns:
        doubled number.
    """
    return x * 2

def multiply_two_nums(x, y):
    """Multiply two numbers together.
    
    Args:
        x: a number.
        y: a number.
    
    Returns:
        x*y.
    """
    return x * y