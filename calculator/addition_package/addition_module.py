def increment_num(x):
    """Increments a number by 1.
    
    Args:
        x: number to be incremented.
    
    Returns:
        incremented number.
    """
    return x + 1

def add_two_nums(x, y):
    """Adds two numbers together.
    
    Args:
        x: a number.
        y: a number.
    
    Returns:
        x+y.
    """
    return x + y
